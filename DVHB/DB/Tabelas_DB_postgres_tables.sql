CREATE EXTENSION IF NOT EXISTS "pgcrypto";

CREATE TABLE IF NOT EXISTS usuarios (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(255) NOT NULL,
    email VARCHAR(255) UNIQUE NOT NULL,
    cpf VARCHAR(14) UNIQUE NOT NULL,
    telefone VARCHAR(15),
    senha VARCHAR(255) NOT NULL,
    foto_perfil VARCHAR(255),
    data_criacao TIMESTAMPTZ DEFAULT NOW(),
    data_ultimo_acesso TIMESTAMPTZ,
    ativo BOOLEAN DEFAULT TRUE
);


CREATE TABLE IF NOT EXISTS enderecos (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario_id INTEGER NOT NULL,
    tipo TEXT DEFAULT 'residencial' CHECK (tipo IN ('residencial', 'comercial', 'outro')),
    endereco VARCHAR(255) NOT NULL,
    numero VARCHAR(10) NOT NULL,
    complemento VARCHAR(255),
    bairro VARCHAR(100),
    cidade VARCHAR(100),
    estado VARCHAR(2),
    cep VARCHAR(9),
    principal BOOLEAN DEFAULT FALSE,
    data_criacao TIMESTAMPTZ DEFAULT NOW(),
    FOREIGN KEY (usuario_id) REFERENCES usuarios(id) ON DELETE CASCADE
);


CREATE TABLE IF NOT EXISTS metodos_pagamento (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario_id INTEGER NOT NULL,
    tipo TEXT CHECK (tipo IN ('cartao_credito', 'cartao_debito', 'boleto', 'pix')) NOT NULL,
    titular VARCHAR(255),
    numero_mascado VARCHAR(20),
    mes_validade INTEGER,
    ano_validade INTEGER,
    chave_pix VARCHAR(255),
    padrao BOOLEAN DEFAULT FALSE,
    ativo BOOLEAN DEFAULT TRUE,
    data_criacao TIMESTAMPTZ DEFAULT NOW(),
    FOREIGN KEY (usuario_id) REFERENCES usuarios(id) ON DELETE CASCADE
);


CREATE TABLE IF NOT EXISTS produtos (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(255) NOT NULL,
    descricao TEXT,
    categoria VARCHAR(100),
    preco NUMERIC(10, 2) NOT NULL,
    preco_original NUMERIC(10, 2),
    estoque INTEGER DEFAULT 0,
    imagem_principal VARCHAR(255),
    rating NUMERIC(3, 2) DEFAULT 0,
    quantidade_avaliacoes INTEGER DEFAULT 0,
    ativo BOOLEAN DEFAULT TRUE,
    data_criacao TIMESTAMPTZ DEFAULT NOW()
);


CREATE TABLE IF NOT EXISTS carrinho (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario_id INTEGER NOT NULL,
    produto_id INTEGER NOT NULL,
    quantidade INTEGER NOT NULL DEFAULT 1,
    preco_unitario NUMERIC(10, 2),
    data_adicao TIMESTAMPTZ DEFAULT NOW(),
    FOREIGN KEY (usuario_id) REFERENCES usuarios(id) ON DELETE CASCADE,
    FOREIGN KEY (produto_id) REFERENCES produtos(id) ON DELETE CASCADE,
    UNIQUE (usuario_id, produto_id)
);


CREATE TABLE IF NOT EXISTS cupons (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    codigo VARCHAR(50) UNIQUE NOT NULL,
    descricao VARCHAR(255),
    tipo TEXT DEFAULT 'percentual' CHECK (tipo IN ('percentual', 'fixo')),
    valor NUMERIC(10, 2) NOT NULL,
    minimo_compra NUMERIC(10, 2),
    quantidade_total INTEGER,
    quantidade_usada INTEGER DEFAULT 0,
    usuario_id INTEGER,
    data_inicio DATE,
    data_fim DATE,
    ativo BOOLEAN DEFAULT TRUE,
    data_criacao TIMESTAMPTZ DEFAULT NOW(),
    FOREIGN KEY (usuario_id) REFERENCES usuarios(id) ON DELETE SET NULL
);


CREATE TABLE IF NOT EXISTS pedidos (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario_id INTEGER NOT NULL,
    endereco_entrega_id INTEGER NOT NULL,
    metodo_pagamento_id INTEGER NOT NULL,
    cupom_id INTEGER,
    status TEXT DEFAULT 'pendente' CHECK (status IN ('pendente', 'processando', 'enviado', 'entregue', 'cancelado', 'devolvido')),
    subtotal NUMERIC(10, 2) NOT NULL,
    desconto NUMERIC(10, 2) DEFAULT 0,
    taxa_entrega NUMERIC(10, 2),
    total NUMERIC(10, 2) NOT NULL,
    numero_rastreio VARCHAR(50),
    data_pedido TIMESTAMPTZ DEFAULT NOW(),
    data_entrega_prevista DATE,
    data_entrega_real DATE,
    data_cancelamento DATE,
    FOREIGN KEY (usuario_id) REFERENCES usuarios(id),
    FOREIGN KEY (endereco_entrega_id) REFERENCES enderecos(id),
    FOREIGN KEY (metodo_pagamento_id) REFERENCES metodos_pagamento(id),
    FOREIGN KEY (cupom_id) REFERENCES cupons(id) ON DELETE SET NULL
);



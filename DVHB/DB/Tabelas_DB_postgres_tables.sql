    instancia_id INTEGER NOT NULL,
    tipo_acesso TEXT DEFAULT 'admin' CHECK (tipo_acesso IN ('admin', 'usuario', 'desenvolvedor', 'suporte', 'leitura')),
    username VARCHAR(255),
    email_acesso VARCHAR(255),
    senha_hash VARCHAR(255),
    senha_temporaria BOOLEAN DEFAULT FALSE,
    data_criacao_acesso TIMESTAMPTZ DEFAULT NOW(),

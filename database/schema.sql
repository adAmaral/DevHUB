CREATE TYPE tipo_usuario AS ENUM ('cliente', 'empresa', 'freelancer');
CREATE TYPE tipo_servico AS ENUM ('servico', 'produto');
CREATE TYPE status_contrato AS ENUM ('pendente', 'aceito', 'em_andamento', 'concluido', 'cancelado');


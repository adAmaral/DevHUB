CREATE TABLE categorias (
    id_categoria INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(100) NOT NULL UNIQUE,
    descricao TEXT,
    icone VARCHAR(100),
    ativo BOOLEAN DEFAULT TRUE,
    data_cadastro TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE servicos (
    id_servico INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario_id INT NOT NULL,
    categoria_id INT NOT NULL,
    titulo VARCHAR(255) NOT NULL,
    descricao TEXT,
    tipo tipo_servico NOT NULL,
    preco DECIMAL(10,2) NOT NULL,
    prazo_entrega INT,
    imagens JSONB,
    tags JSONB,
    ativo BOOLEAN DEFAULT TRUE,
    destaque BOOLEAN DEFAULT FALSE,
    visualizacoes INT DEFAULT 0,
    data_cadastro TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    data_atualizacao TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    busca_tsvector TSVECTOR GENERATED ALWAYS AS (to_tsvector('portuguese', COALESCE(titulo, '') || ' ' || COALESCE(descricao, ''))) STORED,
    FOREIGN KEY (usuario_id) REFERENCES usuarios(id_usuario) ON DELETE CASCADE,
    FOREIGN KEY (categoria_id) REFERENCES categorias(id_categoria) ON DELETE RESTRICT
);

CREATE TABLE avaliacoes (
    id_avaliacao INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    servico_id INT NOT NULL,
    usuario_avaliador_id INT NOT NULL,
    usuario_avaliado_id INT NOT NULL,
    nota INT NOT NULL CHECK (nota >= 1 AND nota <= 5),
    comentario TEXT,
    data_avaliacao TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (servico_id) REFERENCES servicos(id_servico) ON DELETE CASCADE,
    FOREIGN KEY (usuario_avaliador_id) REFERENCES usuarios(id_usuario) ON DELETE CASCADE,
    FOREIGN KEY (usuario_avaliado_id) REFERENCES usuarios(id_usuario) ON DELETE CASCADE,
    UNIQUE (servico_id, usuario_avaliador_id)
);

CREATE TABLE mensagens (
    id_mensagem INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    remetente_id INT NOT NULL,
    destinatario_id INT NOT NULL,
    servico_id INT,
    assunto VARCHAR(255),
    mensagem TEXT NOT NULL,
    lida BOOLEAN DEFAULT FALSE,
    data_envio TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (remetente_id) REFERENCES usuarios(id_usuario) ON DELETE CASCADE,
    FOREIGN KEY (destinatario_id) REFERENCES usuarios(id_usuario) ON DELETE CASCADE,
    FOREIGN KEY (servico_id) REFERENCES servicos(id_servico) ON DELETE SET NULL
);

CREATE TABLE contratos (
    id_contrato INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    servico_id INT NOT NULL,
    cliente_id INT NOT NULL,
    prestador_id INT NOT NULL,
    titulo VARCHAR(255) NOT NULL,
    descricao TEXT,
    valor DECIMAL(10,2) NOT NULL,
    prazo_entrega DATE,
    status status_contrato DEFAULT 'pendente',
    data_criacao TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    data_aceite TIMESTAMP NULL,
    data_conclusao TIMESTAMP NULL,
    data_cancelamento TIMESTAMP NULL,
    motivo_cancelamento TEXT,
    FOREIGN KEY (servico_id) REFERENCES servicos(id_servico) ON DELETE RESTRICT,
    FOREIGN KEY (cliente_id) REFERENCES usuarios(id_usuario) ON DELETE RESTRICT,
    FOREIGN KEY (prestador_id) REFERENCES usuarios(id_usuario) ON DELETE RESTRICT
);

CREATE TABLE favoritos (
    id_favorito INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario_id INT NOT NULL,
    servico_id INT NOT NULL,
    data_favoritado TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (usuario_id) REFERENCES usuarios(id_usuario) ON DELETE CASCADE,
    FOREIGN KEY (servico_id) REFERENCES servicos(id_servico) ON DELETE CASCADE,
    UNIQUE (usuario_id, servico_id)
);

